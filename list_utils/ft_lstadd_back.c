#include "../fdf.h"

void	ft_lstadd_back(t_parse **lst, t_parse *new)
{
	t_parse	*tmp;

	if (!new || !lst)
		return ;
	if (!*lst)
	{
		*lst = new;
		return ;
	}
	tmp = *lst;
	while (tmp->next)
		tmp = tmp->next;
	tmp->next = new;
}
